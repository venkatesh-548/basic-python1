#inheritance
class Emp(object):
    def setid(self,id):
        self.id=id
    def setname(self,name):
        self.name=name
    def setaddress(self,address):
        self.address=address
    def getid(self):
        return self.id
    def getname(self):
        return self.name
    def getaddress(self):
        return self.address
    
        
